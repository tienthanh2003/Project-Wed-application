package Models;

/**
 *
 * @author admin
 */
public class shipper {
    private int id ;
    private String name;

    public shipper() {
    }

    public shipper(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "shipper{" + "id=" + id + ", name=" + name + '}';
    }
   
}
