    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        RequestDispatcher requestDispatcher = request.getRequestDispatcher("Views/LoginUser.jsp");
        requestDispatcher.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO dao = new DAO();
        HttpSession session = request.getSession();
        String userName = request.getParameter("user");
        String passWord = request.getParameter("pass");
        User user = dao.login(userName, passWord);
        if (user == null) {
            // neu sai tk mk
            request.setAttribute("errol", "Nhập sai TK hoặc MK vui lòng nhập lại!");
            request.getRequestDispatcher("Views/LoginUser.jsp").forward(request, response);
        } else {
            //co tim thay 
            // add vao  session
            if (user.getRole() == 2) {
                request.setAttribute("errol", "Tài khoản của bạn đã bị cấm!");
                request.getRequestDispatcher("Views/LoginUser.jsp").forward(request, response);
            } else {
                session.setAttribute("account", user);
                // cho thoi gian toan tai cua session la 1 ngay
                session.setMaxInactiveInterval(60 * 60 * 24);
                response.sendRedirect("home");
            }

        }

    }

